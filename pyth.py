from transformers import pipeline

# Φόρτωση του μοντέλου για ερωτήσεις και απαντήσεις
nlp = pipeline("question-answering", model="distilbert-base-cased-distilled-squad")

# Κείμενο για ανάλυση
context = """
Ο Χρήστος είναι ειδικός στην πληροφορική με εμπειρία στην ανάπτυξη λογισμικού και την τεχνητή νοημοσύνη.
Σπούδασε στο Πανεπιστήμιο Αθηνών και έχει εργαστεί σε πολλές εταιρείες τεχνολογίας.
Οι βασικές του δεξιότητες περιλαμβάνουν προγραμματισμό σε Python, ανάλυση δεδομένων και ανάπτυξη εφαρμογών ιστού.
"""

# Λίστα ερωτήσεων
questions = [
    "Ποιο Πανεπιστήμιο σπούδασε ο Χρήστος;",
    "Ποιες είναι οι βασικές του δεξιότητες;",
    "Που εργάζεται ο Χρήστος;"
]

# Επεξεργασία και εμφάνιση απαντήσεων
for question in questions:
    result = nlp(question=question, context=context)
    print(f"Ερώτηση: {question} | Απάντηση: {result['answer']}")
